import Foundation
import UIKit

extension UIColor {
    class var iVarGreen: UIColor {
        return UIColor(red:19/255, green:135/255, blue:73/255, alpha: 1)
    }
    
    class var iVarRed: UIColor {
        return UIColor(red:168/255, green:42/255, blue:42/255, alpha: 1)
    }
}
